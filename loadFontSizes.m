function [largeFontSize,regularFontSize,smallFontSize] = loadFontSizes()
% LOADFONTSIZES: Defines the core font sizes for an app.
% - largeFontSize:   Returns the app's large (title) font size
% - regularFontSize: Returns the app's regular font size.
% - smallFontSize:   Returns the app's small (caption) font size.

largeFontSize = 36;
regularFontSize = 18;
smallFontSize = 14;
end